          <p class="lead">Mon objectif de stage, était de permettre aux membres de l’entreprise de pouvoir se connecter et paramétrer les machines grâce à un code d’accès. J’ai travaillé en individuel sur cette application, avec parfois, l’aide de mon maitre de stage pour répondre à mes différentes questions. Pour ce faire, j’ai dû créer une application. Cette application devait permettre aux membres de l’entreprise de pouvoir se connecter avec leurs identifiants au générateur afin de pouvoir le paramétrer directement, grâce à un mot de passe et durant un délai illimité. 
               Cette mission m’a été confié car auparavant, quand les membres de l’entreprise se connectaient sur le générateur, ils étaient automatiquement déconnectés au bout de dix minutes, ce qui obligeait l’utilisateur à se reconnecter parfois à plusieurs reprises. 
               J’ai donc dû trouver une solution pour que la connexion soit permanente une fois connecter au générateur afin de faciliter le travail aux utilisateurs et ne pas risquer de perdre du temps entre chaque connexion. 
               L’impact positif de cette application sur l’entreprise est donc de faire gagner du temps aux salariés, en ne s’identifiant pas à chaque connexion, mais aussi de leur faciliter l’accès aux paramétrages du générateur. La non-réussite de l’application aura pour conséquence de rester sur l’ancien modèle de l’application ce qui va générer une perte de temps et d’énergie pour les salariés. 
